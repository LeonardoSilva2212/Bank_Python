# Operadores Aritméticos
a = 10
b = 3

soma = a + b          # Adição
subtracao = a - b     # Subtração
multiplicacao = a * b # Multiplicação
divisao = a / b       # Divisão
modulo = a % b        # Módulo (resto da divisão)
expoente = a ** b     # Exponenciação
div_inteira = a // b  # Divisão inteira


# Operadores de Comparação
maior = a > b          # Maior que
menor = a < b          # Menor que
igual = a == b         # Igual a
diferente = a != b     # Diferente de
maior_igual = a >= b   # Maior ou igual a
menor_igual = a <= b   # Menor ou igual a


# Operadores de Atribuição
c = 10     # Atribuição simples
c += 2     # Atribuição com adição: c = c + 2
c -= 2     # Atribuição com subtração: c = c - 2
c *= 2     # Atribuição com multiplicação: c = c * 2
c /= 2     # Atribuição com divisão: c = c / 2
c %= 2     # Atribuição com módulo: c = c % 2
c **= 2    # Atribuição com exponenciação: c = c ** 2
c //= 2    # Atribuição com divisão inteira: c = c // 2


# Operadores Lógicos
x = True
y = False

and_op = x and y  # Operador lógico AND
or_op = x or y    # Operador lógico OR
not_op = not x    # Operador lógico NOT


# Operadores de Identidade
d = [1, 2, 3]
e = d
f = [1, 2, 3]

identidade_igual = d is e       # Verifica se 'd' e 'e' são o mesmo objeto
identidade_diferente = d is not f  # Verifica se 'd' e 'f' não são o mesmo objeto


# Operadores de Pertinência (ou Associação)
elemento = 2
pertence = elemento in d          # Verifica se 'elemento' está em 'd'
nao_pertence = elemento not in f   # Verifica se 'elemento' não está em 'f'
